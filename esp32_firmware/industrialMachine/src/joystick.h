/*
 * joystick.h — HW-504 аналог joystick-ийг 4 чиглэлийн товч мэт уншина.
 * ----------------------------------------------------------------------
 *  Холболт (ESP32-S3, ADC1 — WiFi-тэй зөрчилдөхгүй):
 *    VRx → GPIO4  (зүүн=буцах / баруун=сонгож ажиллуулах)
 *    VRy → GPIO5  (дээш/доош = цэсээр явах)
 *    SW  → GPIO6  (товч — одоохондоо ашиглахгүй)
 *    VCC → 3V3, GND → GND
 *
 *  BOOT товчтой ЗЭРЭГЦЭЭ ажиллана — аль нэгээр нь цэсийг удирдаж болно.
 *  poll() нь түлхэлт бүрд НЭГ удаа чиглэл буцаана (edge); дараагийн event-ийн
 *  өмнө гол төв рүү буцах ёстой.
 */
#pragma once
#include <Arduino.h>

namespace joy {

enum Dir { NONE, UP, DOWN, LEFT, RIGHT };

void begin();
Dir  poll();           // түлхэлт бүрд нэг event, эс бөгөөс NONE
bool swPressed();      // SW товч дарагдсан эсэх (одоохондоо ашиглахгүй)

} // namespace joy
